import { execFileSync, spawnSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";
import process from "node:process";
import { ensureSmokeVaultReady, openObsidianVault, resolveSmokeVaultPath } from "./obsidian-vault-utils.mjs";

const repoRoot = process.cwd();
const vaultPath = resolveSmokeVaultPath();
process.env.OPENAGENT_OBSIDIAN_VAULT ||= vaultPath;
const smokeVaultRegistration = ensureSmokeVaultReady(vaultPath);
const smokeDirName = "OpenAgent Smoke";
const smokeDir = path.join(vaultPath, smokeDirName);
const canvasPathRelative = `${smokeDirName}/smoke.canvas`;
const notePathRelative = `${smokeDirName}/context.md`;
const commandName = "New thread from selection";
const daemonConfig = loadDaemonConfig();

await main();

async function main() {
  syncPlugin();
  writeFixtureFiles();
  restartObsidian();

  const existingTaskIds = new Set((await getSmokeTasks()).map((task) => task.taskId));

  openSmokeCanvas();
  focusCanvasArea();
  runOpenAgentCommand(commandName);

  const task = await waitForNewSmokeTask(existingTaskIds, 45_000);
  console.log(`Obsidian UI smoke test passed for ${task.taskId}`);
  console.log(JSON.stringify({
    taskId: task.taskId,
    status: task.status,
    updatedAt: task.updatedAt,
    sourceRef: task.sourceRef,
    nodeIds: task.selectionContext?.nodeIds || [],
  }, null, 2));
}

function syncPlugin() {
  execFileSync(process.execPath, [path.join(repoRoot, "scripts", "sync-obsidian-plugin.mjs")], {
    cwd: repoRoot,
    stdio: "inherit",
  });
}

function writeFixtureFiles() {
  fs.mkdirSync(smokeDir, { recursive: true });

  const noteContent = [
    "# OpenAgent Obsidian UI smoke context",
    "",
    "This note is generated by the UI smoke harness.",
    `Repository under test: ${repoRoot}`,
    "",
    "Expected behavior: the UI flow should select this canvas, run the OpenAgent command, and create a new task.",
    "",
  ].join("\n");

  const canvas = {
    nodes: [
      {
        id: "smoke-text",
        type: "text",
        x: 0,
        y: 0,
        width: 420,
        height: 180,
        text: "OpenAgent UI smoke test. Select this canvas and create a new thread.",
      },
      {
        id: "smoke-file",
        type: "file",
        x: 480,
        y: 0,
        width: 420,
        height: 260,
        file: notePathRelative,
      },
    ],
    edges: [],
  };

  fs.writeFileSync(path.join(smokeDir, "context.md"), noteContent, "utf8");
  fs.writeFileSync(path.join(smokeDir, "smoke.canvas"), `${JSON.stringify(canvas, null, 2)}\n`, "utf8");
}

function restartObsidian() {
  spawnSync("osascript", ["-e", 'tell application "Obsidian" to quit'], { stdio: "ignore" });
  waitForObsidianProcess(false, 15_000);
  execFileSync("open", ["-a", "Obsidian"], { stdio: "ignore" });
  openObsidianVault(vaultPath, { vaultId: smokeVaultRegistration.vaultId });
  waitForObsidianProcess(true, 20_000);
  sleep(5_000);
}

function openSmokeCanvas() {
  execFileSync("open", [`obsidian://open?vault=${encodeURIComponent(path.basename(vaultPath))}&file=${encodeURIComponent(canvasPathRelative)}`], {
    stdio: "ignore",
  });
  sleep(3_000);
}

function focusCanvasArea() {
  execFileSync("swift", [
    "-e",
    [
      "import ApplicationServices",
      "let point = CGPoint(x: 500, y: 300)",
      "let move = CGEvent(mouseEventSource: nil, mouseType: .mouseMoved, mouseCursorPosition: point, mouseButton: .left)!",
      "move.post(tap: .cghidEventTap)",
      "let down = CGEvent(mouseEventSource: nil, mouseType: .leftMouseDown, mouseCursorPosition: point, mouseButton: .left)!",
      "down.post(tap: .cghidEventTap)",
      "let up = CGEvent(mouseEventSource: nil, mouseType: .leftMouseUp, mouseCursorPosition: point, mouseButton: .left)!",
      "up.post(tap: .cghidEventTap)",
    ].join("\n"),
  ], { stdio: "ignore" });
  sleep(500);
}

function runOpenAgentCommand(name) {
  execFileSync("osascript", [
    "-e",
    `tell application "Obsidian" to activate`,
    "-e",
    "delay 0.3",
    "-e",
    `tell application "System Events"
  keystroke "a" using command down
  delay 0.5
  keystroke "p" using command down
  delay 0.4
  keystroke "${escapeAppleScriptString(name)}"
  delay 0.4
  key code 36
end tell`,
  ], { stdio: "ignore" });
}

async function waitForNewSmokeTask(existingTaskIds, timeoutMs) {
  const deadline = Date.now() + timeoutMs;
  while (Date.now() < deadline) {
    const task = (await getSmokeTasks()).find((candidate) => !existingTaskIds.has(candidate.taskId));
    if (task) {
      return task;
    }

    sleep(1_000);
  }

  throw new Error("Timed out waiting for a new UI smoke task.");
}

async function getSmokeTasks() {
  const response = await fetch(`http://${daemonConfig.host}:${daemonConfig.port}/tasks`, {
    headers: {
      "x-openagent-token": daemonConfig.token,
    },
  });
  if (!response.ok) {
    throw new Error(`Failed to fetch smoke tasks (${response.status}).`);
  }

  const payload = await response.json();
  return (payload.tasks || []).filter((task) => task.sourceRef === canvasPathRelative);
}

function loadDaemonConfig() {
  const configPath = path.join(os.homedir(), ".openagent", "daemon-config.json");
  return JSON.parse(fs.readFileSync(configPath, "utf8"));
}

function waitForObsidianProcess(shouldExist, timeoutMs) {
  const deadline = Date.now() + timeoutMs;
  while (Date.now() < deadline) {
    const running = spawnSync("pgrep", ["-x", "Obsidian"], { stdio: "ignore" }).status === 0;
    if (running === shouldExist) {
      return;
    }

    sleep(500);
  }

  throw new Error(shouldExist ? "Obsidian did not start in time." : "Obsidian did not quit in time.");
}

function escapeAppleScriptString(value) {
  return String(value).replaceAll("\\", "\\\\").replaceAll("\"", "\\\"");
}

function sleep(ms) {
  const shared = new SharedArrayBuffer(4);
  const view = new Int32Array(shared);
  Atomics.wait(view, 0, 0, ms);
}
